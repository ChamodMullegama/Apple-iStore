    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String username = U_Name.getText();
        String password = Get_Password.getText();
        String userType = (String)Usear_Type.getSelectedItem();

        user userobj = new user();
      
        
        if (userobj.LOGIN(username, password, userType)) {
            if (userType.equals("Manager")) {
                // Open Manager panel
                 Manger_Page ManagerObj = new Manger_Page();
                 ManagerObj.setVisible(true);
                 this.dispose();
               
            } else if (userType.equals("Cashier")) {
                // Open cashier panel
                 Cashier CashierObj = new Cashier();
                 CashierObj.setVisible(true);
                 this.dispose();
                
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Invalid username or password.");
        }      
    }//GEN-LAST:event_jButton1ActionPerformed

